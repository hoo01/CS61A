def divide(quotients, divisors):
    """Return a dictonary in which each quotient q is a key for the list of
    divisors that it divides evenly.

    >>> divide([3, 4, 5], [8, 9, 10, 11, 12])
    {3: [9, 12], 4: [8, 12], 5: [10]}
    >>> divide(range(1, 5), range(20, 25))
    {1: [20, 21, 22, 23, 24], 2: [20, 22, 24], 3: [21, 24], 4: [20, 24]}
    """
    result = {}  # 创建一个空字典来存储结果

    for x in quotients:  # 遍历 quotients 列表中的每个商数 x
        result[x] = []  # 为每个商数 x 初始化一个空列表，准备存储能被 x 整除的除数

        for y in divisors:  # 遍历 divisors 列表中的每个除数 y
            if y % x == 0:  # 如果除数 y 能被商数 x 整除
                result[x].append(y)  # 将该除数 y 加入到对应商数 x 的列表中

    return result  # 返回生成的字典


